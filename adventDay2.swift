func runPart1() -> Int {
    var score = 0
    for game in array {
        let round = game.components(separatedBy: " ")

        let opponent = Plays(opponent: round[0]) 
        let player = Plays(player: round[1])

        let roundResult = roundResult(opponent, player)

        score += roundResult.rawValue + player.rawValue
    }

    return score

func roundResult(_ opponent: Plays, _ player: Plays) -> Result {
    if opponent == player {
        return .draw
    }

    if  (opponent == .rock && player == .scissors) ||
        (opponent == .paper && player == .rock) ||
        (opponent == .scissors && player == .paper) {
            return .lost
    }

    return .won
}


enum Result: Int {
    case lost = 0
    case draw = 3
    case won = 6
}

enum Plays: Int {
    case rock = 1
    case paper = 2
    case scissors = 3

    init(opponent: String) {
        switch opponent {
        case "A":
        self = .rock

        case "B":
        self = .paper

        case "C": 
        self = .scissors

        default:
            fatalError()
        }
    }

/*

X - lose
Y - draw
Z - win

*/
    init(player: String) {
        switch player {
        case "X":
        self = .rock

        case "Y":
        self = .paper

        case "Z": 
        self = .scissors

        default:
            fatalError()
        }
    }
}



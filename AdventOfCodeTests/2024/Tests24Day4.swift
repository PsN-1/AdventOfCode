    func testDay2Pt2() { solutionDay4Pt2(input: input, expected: 1916) }
    
    func solutionDay4Pt2(input: String, expected: Int) {
        let arr = input.components(separatedBy: "\n").map { Array($0) }
        var count = 0
        
        for i in 0..<arr.count {
            for j in 0..<arr.count {
                guard arr[i][j] == "A" else { continue }
                guard (i+1) < arr.count && (i-1) >= 0 && (j+1) < arr.count && (j-1) >= 0 else { continue }
                
                if (arr[i-1][j-1] == "M" && arr[i+1][j+1] == "S" || arr[i+1][j+1] == "M" && arr[i-1][j-1] == "S")
                && (arr[i+1][j-1] == "M" && arr[i-1][j+1] == "S" || arr[i-1][j+1] == "M" && arr[i+1][j-1] == "S") {
                    count += 1
                }
            }
        }
        
        XCTAssertEqual(count, expected)
    }
    
    func solutionDay4(input: String, expected: Int) {
        let arr = input.components(separatedBy: "\n").map { Array($0) }
        var count = 0
        
        for i in 0..<arr.count {
            for j in 0..<arr.count {
                if arr[i][j] == "X" {
                    // horizontal
                    if j + 3 < arr.count && arr[i][j+1] == "M" && arr[i][j+2] == "A" && arr[i][j+3] == "S" {
                        count += 1
                    }
                    if j - 3 >= 0 && arr[i][j-1] == "M" && arr[i][j-2] == "A" && arr[i][j-3] == "S" {
                        count += 1
                    }
                    
                    // vertical
                    if i + 3 < arr.count && arr[i+1][j] == "M" && arr[i+2][j] == "A" && arr[i+3][j] == "S" {
                        count += 1
                    }
                    if i - 3 >= 0 && arr[i-1][j] == "M" && arr[i-2][j] == "A" && arr[i-3][j] == "S" {
                        count += 1
                    }

                    // diagonal
                    if i + 3 < arr.count && j + 3 < arr.count && arr[i+1][j+1] == "M" && arr[i+2][j+2] == "A" && arr[i+3][j+3] == "S" {
                        count += 1
                    }
                    if i - 3 >= 0 && j-3 >= 0 && arr[i-1][j-1] == "M" && arr[i-2][j-2] == "A" && arr[i-3][j-3] == "S" {
                        count += 1
                    }
                    
                    // opposite diagonal
                    if i - 3 >= 0 && j + 3 < arr.count && arr[i-1][j+1] == "M" && arr[i-2][j+2] == "A" && arr[i-3][j+3] == "S" {
                        count += 1
                    }
                    if j - 3 >= 0 && i + 3 < arr.count && arr[i+1][j-1] == "M" && arr[i+2][j-2] == "A" && arr[i+3][j-3] == "S" {
                        count += 1
                    }
                }
            }
        }
        
        XCTAssertEqual(count, expected)
    }
    
